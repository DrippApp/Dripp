import UIKit

extension UIColor {
    
    class var blue:UIColor {
        get {
            return UIColor(red:0.421593, green: 0.657718, blue: 0.972549, alpha: 1)
        }
    }
    
    class var lightBlue:UIColor {
        get {
            return UIColor(red:0.700062, green: 0.817345, blue: 0.972549, alpha: 1)
        }
    }
    
}